void main() {
  // Membuat list berisi tipe data int (kosong)
  List<int> listInt = [];

  // Membuat list berisi tipe data String (kosong)
  List<String> listString = [];

  // Contoh menambahkan data ke dalam list
  listInt.add(10);
  listInt.add(20);
  listString.add('Ervina');
  listString.add('Shaleha');

  // Menampilkan isi list
  print(listInt);     // Output: [10, 20]
  print(listString);  // Output: [Ervina, Shaleha]
}
void main() {
  // Membuat list dengan tipe data int
  List<int> listInt = [];

  // Membuat list dengan tipe data String
  List<String> listString = [];

  // Menambahkan data ke list
  listInt.add(10);
  listInt.add(20);
  listInt.add(30);

  listString.add('Ervina');
  listString.add('Shaleha');
  listString.add('Ebynaa');

  // Menampilkan isi list
  print(listInt);     // Output: [10, 20, 30]
  print(listString);  // Output: [Ervina, Shaleha, Ebynaa]
}